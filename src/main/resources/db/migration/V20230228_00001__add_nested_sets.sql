CREATE TABLE IF NOT EXISTS nested_sets (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   name VARCHAR(255) NOT NULL,
   left_value BIGINT NOT NULL,
   right_value BIGINT NOT NULL,
   CONSTRAINT pk_nested_sets PRIMARY KEY (id)
);

CREATE INDEX IDX_LEFT_VALUE ON nested_sets(left_value);

CREATE INDEX IDX_RIGHT_VALUE ON nested_sets(right_value);

ALTER TABLE adjacency_tree ALTER COLUMN  name SET NOT NULL;

ALTER TABLE node_name ALTER COLUMN  name SET NOT NULL;